-- 2. DATABASE TRIGGERS & AUTOMATION
-- =====================================================================

-- Trigger: Automatically sync newly created Supabase Auth users to public.users profiles
CREATE OR REPLACE FUNCTION public.handle_new_user()
RETURNS TRIGGER AS $$
BEGIN
  INSERT INTO public.users (id, email, full_name, avatar_url, role)
  VALUES (
    NEW.id,
    NEW.email,
    COALESCE(NEW.raw_user_meta_data->>'full_name', 'Học viên mới'),
    COALESCE(NEW.raw_user_meta_data->>'avatar_url', ''),
    COALESCE(NEW.raw_user_meta_data->>'role', 'student')
  );
  RETURN NEW;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

CREATE TRIGGER on_auth_user_created
  AFTER INSERT ON auth.users
  FOR EACH ROW EXECUTE FUNCTION public.handle_new_user();


-- Trigger: Increment or decrement courses.enrolled_count automatically on enrollments change
CREATE OR REPLACE FUNCTION public.update_course_enrollment_count()
RETURNS TRIGGER AS $$
BEGIN
  IF TG_OP = 'INSERT' THEN
    UPDATE public.courses
    SET enrolled_count = enrolled_count + 1
    WHERE id = NEW.course_id;
  ELSIF TG_OP = 'DELETE' THEN
    UPDATE public.courses
    SET enrolled_count = GREATEST(0, enrolled_count - 1)
    WHERE id = OLD.course_id;
  END IF;
  RETURN NULL;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

CREATE TRIGGER on_enrollment_changed
  AFTER INSERT OR DELETE ON public.enrollments
  FOR EACH ROW EXECUTE FUNCTION public.update_course_enrollment_count();


-- Trigger: Auto-generate certificate and set completed_at timestamp when progress_percent hits 100%
CREATE OR REPLACE FUNCTION public.generate_certificate_on_completion()
RETURNS TRIGGER AS $$
BEGIN
  IF NEW.progress_percent = 100 AND (OLD.progress_percent IS NULL OR OLD.progress_percent < 100) THEN
    -- Update completed timestamp
    NEW.completed_at = NOW();
    
    -- Insert certificate if not exists
    INSERT INTO public.certificates (user_id, course_id, certificate_url)
    VALUES (
      NEW.user_id,
      NEW.course_id,
      'https://certificates.learningapp.com/verify/' || NEW.user_id || '/' || NEW.course_id
    ) ON CONFLICT ON CONSTRAINT unique_user_course_certificate DO NOTHING;
  END IF;
  RETURN NEW;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

CREATE TRIGGER on_enrollment_completed
  BEFORE UPDATE ON public.enrollments
  FOR EACH ROW EXECUTE FUNCTION public.generate_certificate_on_completion();

