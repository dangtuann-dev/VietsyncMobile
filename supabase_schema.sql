-- 4.1 USERS POLICIES
CREATE POLICY "Allow public read access to users profiles" 
  ON public.users FOR SELECT TO authenticated, anon USING (true);

CREATE POLICY "Allow users to update their own profile" 
  ON public.users FOR UPDATE TO authenticated USING (auth.uid() = id);

-- 4.2 CATEGORIES POLICIES
CREATE POLICY "Allow public read access to categories" 
  ON public.categories FOR SELECT TO authenticated, anon USING (true);

CREATE POLICY "Allow admins to manage categories" 
  ON public.categories FOR ALL TO authenticated 
  USING (EXISTS (SELECT 1 FROM public.users WHERE id = auth.uid() AND role = 'admin'));

-- 4.3 COURSES POLICIES
CREATE POLICY "Allow public read access to courses" 
  ON public.courses FOR SELECT TO authenticated, anon USING (true);

CREATE POLICY "Allow instructors/admins to create courses" 
  ON public.courses FOR INSERT TO authenticated 
  WITH CHECK (
    EXISTS (SELECT 1 FROM public.users WHERE id = auth.uid() AND role IN ('instructor', 'admin')) 
    AND instructor_id = auth.uid()
  );

CREATE POLICY "Allow instructors/admins to update their courses" 
  ON public.courses FOR UPDATE TO authenticated 
  USING (
    instructor_id = auth.uid() 
    OR EXISTS (SELECT 1 FROM public.users WHERE id = auth.uid() AND role = 'admin')
  );

CREATE POLICY "Allow instructors/admins to delete their courses" 
  ON public.courses FOR DELETE TO authenticated 
  USING (
    instructor_id = auth.uid() 
    OR EXISTS (SELECT 1 FROM public.users WHERE id = auth.uid() AND role = 'admin')
  );

-- 4.4 LESSONS POLICIES
CREATE POLICY "Allow enrolled students and instructors to view lessons" 
  ON public.lessons FOR SELECT TO authenticated 
  USING (
    EXISTS (
      SELECT 1 FROM public.enrollments e 
      WHERE e.course_id = lessons.course_id AND e.user_id = auth.uid()
    ) OR EXISTS (
      SELECT 1 FROM public.courses c 
      WHERE c.id = lessons.course_id AND (c.instructor_id = auth.uid() OR EXISTS (SELECT 1 FROM public.users WHERE id = auth.uid() AND role = 'admin'))
    )
  );

CREATE POLICY "Allow instructors and admins to manage lessons" 
  ON public.lessons FOR ALL TO authenticated 
  USING (
    EXISTS (
      SELECT 1 FROM public.courses c 
      WHERE c.id = lessons.course_id AND (c.instructor_id = auth.uid() OR EXISTS (SELECT 1 FROM public.users WHERE id = auth.uid() AND role = 'admin'))
    )
  );

-- 4.5 ENROLLMENTS POLICIES
CREATE POLICY "Allow enrolled students, course instructors and admins to view enrollment" 
  ON public.enrollments FOR SELECT TO authenticated 
  USING (
    user_id = auth.uid() 
    OR EXISTS (SELECT 1 FROM public.courses c WHERE c.id = enrollments.course_id AND c.instructor_id = auth.uid())
    OR EXISTS (SELECT 1 FROM public.users WHERE id = auth.uid() AND role = 'admin')
  );

CREATE POLICY "Allow students to enroll themselves" 
  ON public.enrollments FOR INSERT TO authenticated 
  WITH CHECK (user_id = auth.uid());

CREATE POLICY "Allow students/admins to update enrollment progress" 
  ON public.enrollments FOR UPDATE TO authenticated 
  USING (
    user_id = auth.uid() 
    OR EXISTS (SELECT 1 FROM public.users WHERE id = auth.uid() AND role = 'admin')
  );

-- 4.6 QUIZZES POLICIES
CREATE POLICY "Allow enrolled students and instructors to view quizzes" 
  ON public.quizzes FOR SELECT TO authenticated 
  USING (
    EXISTS (
      SELECT 1 FROM public.lessons l
      JOIN public.enrollments e ON e.course_id = l.course_id
      WHERE l.id = quizzes.lesson_id AND e.user_id = auth.uid()
    ) OR EXISTS (
      SELECT 1 FROM public.lessons l
      JOIN public.courses c ON c.id = l.course_id
      WHERE l.id = quizzes.lesson_id AND (c.instructor_id = auth.uid() OR EXISTS (SELECT 1 FROM public.users WHERE id = auth.uid() AND role = 'admin'))
    )
  );

CREATE POLICY "Allow instructors and admins to manage quizzes" 
  ON public.quizzes FOR ALL TO authenticated 
  USING (
    EXISTS (
      SELECT 1 FROM public.lessons l
      JOIN public.courses c ON c.id = l.course_id
      WHERE l.id = quizzes.lesson_id AND (c.instructor_id = auth.uid() OR EXISTS (SELECT 1 FROM public.users WHERE id = auth.uid() AND role = 'admin'))
    )
  );

-- 4.7 QUIZ ATTEMPTS POLICIES
CREATE POLICY "Allow users to view their own quiz attempts" 
  ON public.quiz_attempts FOR SELECT TO authenticated 
  USING (
    user_id = auth.uid() 
    OR EXISTS (SELECT 1 FROM public.users WHERE id = auth.uid() AND role = 'admin')
  );

CREATE POLICY "Allow students to submit quiz attempts" 
  ON public.quiz_attempts FOR INSERT TO authenticated 
  WITH CHECK (user_id = auth.uid());

-- 4.8 CERTIFICATES POLICIES
CREATE POLICY "Allow public read access to certificates" 
  ON public.certificates FOR SELECT TO authenticated, anon USING (true);

CREATE POLICY "Allow admin to manage certificates directly" 
  ON public.certificates FOR ALL TO authenticated 
  USING (EXISTS (SELECT 1 FROM public.users WHERE id = auth.uid() AND role = 'admin'));

