-- =====================================================================
-- 1. TABLE CREATION
-- =====================================================================

-- Users Profile Table (linked to Supabase auth.users)
CREATE TABLE public.users (
    id UUID PRIMARY KEY REFERENCES auth.users(id) ON DELETE CASCADE,
    email TEXT NOT NULL UNIQUE,
    full_name TEXT,
    avatar_url TEXT,
    role TEXT DEFAULT 'student' CHECK (role IN ('student', 'instructor', 'admin')),
    created_at TIMESTAMPTZ DEFAULT NOW() NOT NULL
);

-- Course Categories Table
CREATE TABLE public.categories (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name TEXT NOT NULL UNIQUE,
    icon TEXT,
    color TEXT
);

-- Courses Table
CREATE TABLE public.courses (
    id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
    title TEXT NOT NULL,
    description TEXT,
    thumbnail TEXT,
    instructor_id UUID REFERENCES public.users(id) ON DELETE SET NULL,
    category_id BIGINT REFERENCES public.categories(id) ON DELETE SET NULL,
    price DECIMAL(10, 2) DEFAULT 0.00 NOT NULL CHECK (price >= 0),
    level TEXT DEFAULT 'beginner' CHECK (level IN ('beginner', 'intermediate', 'advanced')),
    duration INT DEFAULT 0 NOT NULL CHECK (duration >= 0), -- total duration in minutes
    rating DECIMAL(3, 2) DEFAULT 0.00 CHECK (rating BETWEEN 0.00 AND 5.00),
    enrolled_count INT DEFAULT 0 NOT NULL CHECK (enrolled_count >= 0)
);

-- Lessons Table
CREATE TABLE public.lessons (
    id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
    course_id UUID REFERENCES public.courses(id) ON DELETE CASCADE NOT NULL,
    title TEXT NOT NULL,
    video_url TEXT,
    content TEXT,
    order_index INT NOT NULL CHECK (order_index >= 0),
    duration INT DEFAULT 0 NOT NULL CHECK (duration >= 0), -- duration in minutes
    CONSTRAINT unique_course_lesson_order UNIQUE (course_id, order_index)
);

-- Course Enrollments Table (Many-to-Many relation between Users and Courses)
CREATE TABLE public.enrollments (
    user_id UUID REFERENCES public.users(id) ON DELETE CASCADE NOT NULL,
    course_id UUID REFERENCES public.courses(id) ON DELETE CASCADE NOT NULL,
    enrolled_at TIMESTAMPTZ DEFAULT NOW() NOT NULL,
    progress_percent INT DEFAULT 0 NOT NULL CHECK (progress_percent BETWEEN 0 AND 100),
    completed_at TIMESTAMPTZ,
    PRIMARY KEY (user_id, course_id)
);

-- Quizzes Table
CREATE TABLE public.quizzes (
    id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
    lesson_id UUID REFERENCES public.lessons(id) ON DELETE CASCADE NOT NULL,
    question TEXT NOT NULL,
    options JSONB NOT NULL, -- Format: ["Option A", "Option B", "Option C", "Option D"]
    correct_answer TEXT NOT NULL,
    explanation TEXT
);

-- Quiz Attempts Table
CREATE TABLE public.quiz_attempts (
    id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
    user_id UUID REFERENCES public.users(id) ON DELETE CASCADE NOT NULL,
    quiz_id UUID REFERENCES public.quizzes(id) ON DELETE CASCADE NOT NULL,
    selected_answer TEXT NOT NULL,
    is_correct BOOLEAN NOT NULL,
    attempted_at TIMESTAMPTZ DEFAULT NOW() NOT NULL
);

-- Certificates Table
CREATE TABLE public.certificates (
    id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
    user_id UUID REFERENCES public.users(id) ON DELETE CASCADE NOT NULL,
    course_id UUID REFERENCES public.courses(id) ON DELETE CASCADE NOT NULL,
    issued_at TIMESTAMPTZ DEFAULT NOW() NOT NULL,
    certificate_url TEXT NOT NULL,
    CONSTRAINT unique_user_course_certificate UNIQUE (user_id, course_id)
);

